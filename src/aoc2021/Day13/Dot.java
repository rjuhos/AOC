package AOC2021.Day13;

public class Dot {
    private int posX;
    private int posY;

    public Dot(int valueX, int valueY) {
        posX = valueX;
        posY = valueY;
    }

    public void setPosX(int value) {
        posX = value;
    }

    public int getPosX() {
        return posX;
    }

    public void setPosY(int value) {
        posY = value;
    }

    public int getPosY() {
        return posY;
    }

}
